      if (hasPendingTool) return 'tool-calling'
      if (isCurrent) return 'generating'
      // ÊúâÂ∑•ÂÖ∑‰∫ã‰ª∂‰∏îÂùáÊúâÁªìÊûú
      return 'tool-completed'
