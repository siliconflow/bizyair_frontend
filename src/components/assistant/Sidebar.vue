    // ÊóßÁâàÔºö‰øùÁïôÂéüÈÄªËæë
    const isCurrentMessage = isGenerating.value && message.rawText !== undefined
    if (isCurrentMessage) {
      if (message.toolName && !message.postToolContent) {
        return 'tool-calling'
      }
      return 'generating'
    }
    if (message.toolName && message.postToolContent) {
      return 'tool-completed'
    }
    return null
  }

