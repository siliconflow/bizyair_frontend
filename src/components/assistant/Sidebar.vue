  const handleImageUpload = async (event: Event) => {
    const target = event.target as HTMLInputElement
    if (!target.files?.length) return

    const file = target.files[0]

    // È™åËØÅÊñá‰ª∂Á±ªÂûã
    if (!file.type.startsWith('image/')) {
